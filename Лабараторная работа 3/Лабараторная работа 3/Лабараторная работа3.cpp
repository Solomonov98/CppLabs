#include <iostream>
#include <fstream>
#include <stdlib.h>
using namespace std;
int main()
{
	int *mas = (int*)calloc(10000, sizeof(int));
	ifstream input("input.txt");
	int len = 0, symbol = 0;
	if (input)
	{
		int zeros = 0;
		while (!input.eof())
		{
			input >> symbol;
			if (symbol == 0)
			{
				zeros++;
			}
			mas[len] = symbol;
			len++;
		}
		mas[len] = zeros;
		len++;
	}
	else
	{
		cout << "No file input.txt" << endl;
	}
	ofstream output("output.txt");
	for (int j = 0; j < len; j++)
	{
		output << mas[j] << " ";
	}
	delete mas;
	input.close();
	output.close();
	return 0;
}
